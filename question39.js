function CityCountry(city, country) {
    return ("".concat(city, ", ").concat(country));
}
var result1 = CityCountry("Karachi", "Pakistan");
var result2 = CityCountry("Tokyo", " japan");
var result3 = CityCountry("paris", "France");
console.log(result1);
console.log(result2);
console.log(result3);
